using Microsoft.EntityFrameworkCore;
using SimpleAPI.DAL.Data;

namespace SimpleAPI.Builders
{
    public static class DatabaseExt
    {

        public static void CreateDbIfNotExists(this WebApplication host)
        {
            using (var scope = host.Services.CreateScope())
            {
                var services = scope.ServiceProvider;
                try
                {
                    var context = services.GetRequiredService<WeatherContext>();
                    DbInitializer.Initialize(context);
                }
                catch (Exception ex)
                {
                    var logger = services.GetRequiredService<ILogger<Program>>();
                    logger.LogError(ex, "An error occurred creating the DB.");
                }
            }
        }
    }
}
